var maxdep;
var c_width = 800;
var c_height = 600;
var dat=[];
var inf = 100000000;
var ratio;
var id = 0;
var rect={
	size: 0,
	childarr:[],
	newobject:function(){
		var temp = Object.create(rect);
		temp.childarr = new Array();
		return temp;
	}
};

function CalcArea(x,dep){
	maxdep = Math.max(maxdep,dep);
	dat.push(rect.newobject()); 
	var id = dat.length - 1;
	dat[id].name = x. name;
	dat[id].id = x.id;
	if (typeof(x.size)=="undefined") {
		x.size = 0;
		for (var i = 0; i < x.children.length; i++){
			var re = CalcArea(x.children[i],dep+1);
			x.size += x.children[i].size;
			dat[id].childarr.push(re);
		}
	}
	dat[id].size = x.size;
	//console.log("~~~~~~~~~~~~",dat[id].size, dat[id].name, dat[id].childarr.length);
	return id;
}
function draw(x, dep){ 
	function layoutrow(row){
		var sum = 0;
		var len = row.length;
		for (var i = 0; i < len; i++){
			sum += dat[row[i]].size;
		}
		if (drawHorizon == 1){
			var _height = sum / totArea * height;
			var _lux = lux;
			for (var i = 0; i < len; i++){
				id = row[i];	
				dat[id].lux = lux;
				dat[id].luy = luy;
				dat[id].height = _height;
				lux += dat[id].size / sum * width;
				dat[id].width = dat[id].size / sum * width;
				//console.log("lay1",id,dat[id].lux,dat[id].luy,dat[id].width,dat[id].height);
			}
			lux = _lux;
			luy += _height;
			height -= _height;
		}else{
			var _width = sum / totArea * width;
			var _luy = luy;
			for (var i = 0; i < len; i++){
				id = row[i];
				
				dat[id].lux = lux;
				dat[id].luy = luy;
				dat[id].width =  _width;
				luy += dat[id].size / sum * height;
				dat[id].height = dat[id].size / sum * height;
				//console.log("lay2",id,dat[id].lux,dat[id].luy,dat[id].width,dat[id].height);
			}
			lux += _width;
			luy = _luy;
			width -= _width;
		}	
		drawHorizon = (width <= height);
		totArea -= sum;		
		
	}
	function squarify(children, row, w){
		if (children.length == 0){
			layoutrow(row);
			return;
		}
		var c = children.shift();
		var t1 = worst(row, w);
		row.push(c);
		var t2 = worst(row, w);
		row.pop(c);
		//console.log("######",t1,t2);
		if (t1 >= t2){
			//console.log("push c",row.length);
			row.push(c);
			squarify(children, row, w);
		}else {
			//console.log("layout",row.length);
			layoutrow(row);
			children.unshift(c);
			row = [];
			squarify(children, row, Math.min(width, height));
		}
	}
	
	var midx = dat[x].lux + dat[x].width / 2;
	var midy = dat[x].luy + dat[x].height/ 2;
	var minr = Math.min(dat[x].width, dat[x].height)/2;
	var rGrd = main_ctx.createRadialGradient(midx, midy, minr/10, midx, midy, minr); 

	if (dep >= 2){
		rGrd.addColorStop(1,    "rgba("+255+","+255+","+255+","+0+")");  
		rGrd.addColorStop(0,    "rgba("+0+","+255+","+0+","+0.2 +")");  
	}
	if (dep == 1){
		rGrd = main_ctx.createRadialGradient(midx, midy, minr/3, midx, midy, minr*10); 
		rGrd.addColorStop(1,    "rgba("+255+","+255+","+255+","+0+")"); 
		rGrd.addColorStop(0.8,  "rgba("+0+","+0+","+0+","+0+")"); 
		rGrd.addColorStop(0,    "rgba("+255+","+255+","+255+","+1+")");  
	}
	main_ctx.fillStyle = rGrd;
	if (dep == 1 )main_ctx.fillRect(dat[x].lux, dat[x].luy, dat[x].width, dat[x].height);
	if (dep >= 2) { main_ctx.beginPath();
					main_ctx.arc(midx, midy, Math.min(minr,32/Math.pow(2,dep)), 0, 2*Math.PI, false);
					main_ctx.fill();
	}
	
	
	main_ctx.strokeStyle = "#EEEE00";	
	main_ctx.beginPath();
	main_ctx.lineWidth = (maxdep - dep + 1);
	main_ctx.moveTo(dat[x].lux, dat[x].luy);
	main_ctx.lineTo(dat[x].lux + dat[x].width, dat[x].luy);
	main_ctx.lineTo(dat[x].lux + dat[x].width, dat[x].luy + dat[x].height);
	main_ctx.lineTo(dat[x].lux, dat[x].luy + dat[x].height);
	main_ctx.closePath();
	main_ctx.stroke();	
	main_ctx.lineWidth = 1;
	main_ctx.strokeStyle = "#000000";


	if (dat[x].childarr.length != 0) {
		var lux = dat[x].lux;
		var luy = dat[x].luy;
		var width = dat[x].width;
		var height = dat[x].height;
		var drawHorizon = (width <= height);
		var totArea = dat[x].size;
		dat[x].childarr.sort(function(a, b){return dat[b].size - dat[a].size;});
		var list = dat[x].childarr.concat();
		var row = [];
		squarify(list, row, Math.min(width, height)); 
		for (var i = 0; i < dat[x].childarr.length; i++)
			draw(dat[x].childarr[i], dep+1);
	}
	main_ctx.textAlign = "center";
	main_ctx.textBaseline = "middle";	
	main_ctx.font = 64/Math.pow(2,dep) + "px Monospace";
	if ((main_ctx.measureText(dat[x].name).width)<dat[x].width)
	{ 	
		main_ctx.strokeStyle = "#FF0000";	
		main_ctx.strokeText(dat[x].name, midx, midy);
		main_ctx.strokeStyle = "#000000";
	}
	
	
}

window.addEventListener("load", function () {
	main_canvas = document.getElementById('can_main');
	main_ctx = main_canvas.getContext('2d');
	maxdep = 0;
	CalcArea(treemap,0);
	ratio = c_width * c_height / dat[0].size;
	dat[0].lux = 0;
	dat[0].luy = 0;
	dat[0].width = c_width;
	dat[0].height = c_height;
	draw(0,0);
	
});



function worst(R, w){
	
	var len = R.length;
	if (len == 0) return inf;
	var min = inf,
		max = 0, 
		sum = 0;
	for (var i = 0; i < len; i++){
		//console.log("~~~~~",R[i]);
		sum += dat[R[i]].size;
		max = Math.max(max,dat[R[i]].size);
		min = Math.min(min,dat[R[i]].size);
	}
	return Math.max(1.0*w*w*max/sum/sum/ratio,1.0*sum*sum*ratio/w/w/min);
	
	
}
